def load_index() -> Dict:
    if INDEX_FILE.exists():
        with open(INDEX_FILE, "r") as f:
            return json.load(f)
    return {
        "version": "1.0.0",
        "last_updated": datetime.now().isoformat(),
        "connectors": []
    }

def save_index(index: Dict):
    index["last_updated"] = datetime.now().isoformat()
    with open(INDEX_FILE, "w") as f:
        json.dump(index, f, indent=2)

def add_to_index(connector_meta: Dict):
    index = load_index()
    connector_meta["uid"] = str(uuid.uuid4())
    connector_meta["created_at"] = datetime.now().isoformat()
    connector_meta["version"] = "1.0.0"
    index["connectors"].append(connector_meta)
    save_index(index)
    return connector_meta["uid"]

